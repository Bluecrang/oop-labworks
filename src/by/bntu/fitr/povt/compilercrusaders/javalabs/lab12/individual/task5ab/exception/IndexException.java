package by.bntu.fitr.povt.compilercrusaders.javalabs.lab12.individual.task5ab.exception;

public class IndexException extends RuntimeException {

	public IndexException() {
		super();
	}

	public IndexException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IndexException(String arg0) {
		super(arg0);
	}

	public IndexException(Throwable arg0) {
		super(arg0);
	}

}
